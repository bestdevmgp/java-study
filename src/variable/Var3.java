package variable;

public class Var3 {
    public static void main(String[] args) {
        int a = 100;                // 정수
        double b = 10.5;            // 실수
        boolean c = true;           // 참과 거짓을 판단할 때 사용 true, false 입력 가능
        char d = 'A';               // 문자 하나, 작은 따옴표를 사용
        String e = "Hello Java";    // 문자열, 큰 따옴표를 사용
    }
}
