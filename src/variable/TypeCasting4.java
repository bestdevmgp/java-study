package variable;

public class TypeCasting4 {
    public static void main(String[] args) {
        byte value6 = (byte)128;
        int value7 = (int)3.5;
        float value8 = (float)7.5;

        System.out.println(value6);
        System.out.println(value7);
        System.out.println(value8);
    }
}
